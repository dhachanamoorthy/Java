public class For_Loop{
    public static void main(String[] args) {
        int x=5;
        //Simple for Loop 
        System.out.println("Before loop x="+x);
        for(int i=0;i<x;i++){
            System.out.println("In loop x="+i);
        }
        System.out.println("After Loop x="+x);
    }
}