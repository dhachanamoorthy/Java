class Rep {
    private int rep_id;
    private String location;

    public void setRepId(int rep_id){
        this.rep_id=rep_id;
    }

    public void setLocation(String location){
        this.location=location;
    }

    public String getLocation(){
        return location;
    }

    public int getRepID(){
        return rep_id;
    }
}
